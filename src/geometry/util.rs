// --------------------------------------------------------------------------------------------------------------------------------------------------
// Barycentric coordinates on a triangle
// --------------------------------------------------------------------------------------------------------------------------------------------------

#[derive(Clone, Copy, Debug)]
pub struct Barycentric {
    pub alpha: f32,
    pub beta: f32,
    pub gamma: f32,
}

// --------------------------------------------------------------------------------------------------------------------------------------------------
// Half-open interval [a,b)
// --------------------------------------------------------------------------------------------------------------------------------------------------

#[derive(Clone, Copy, Debug)]
pub struct Interval {
    pub start: f32,
    pub finish: f32,
}

impl Interval {
    pub fn new(s: f32, f: f32) -> Self {
        Interval {
            start: f32::min(s, f),
            finish: f32::max(s, f),
        }
    }
}
